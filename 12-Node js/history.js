//                                  History of Node.js:

//Node.js Creation:   Node.js was created in 2009 by Ryan Doll. It's a tool that allows JavaScript to run 
// outside of a web browser, using Chrome's V8 engine.


//npm Introduction:     In 2011, npm (node package manager) was introduced. It made it easy to share and
//  manage Node.js code libraries.


//Node.js Foundation:    In 2015, the Node.js Foundation was formed, including big companies like IBM,
//  Microsoft, and PayPal. This helped guide the development and growth of Node.js.


//Community Growth: Today, Node.js has a large, active community with many conferences and events worldwide.



//This tool is now widely used across various industries for building web applications and controlling servers.

